package net.greatsoft.core.web.dto;

public class ProgressDto {
	private String orgId;
	
	private String orgName;
	
	private String fillType;
	
	private String fillTypeName;
	
	private Integer allSum;
	
	private Integer submitSum;
	
	private Integer noSubmitSum;
	
	private Integer passSum;
	
	private Integer sum;
	
	private String uniqueId;
	
	
	
	public String getUniqueId() {
		return uniqueId;
	}
	public void setUniqueId(String uniqueId) {
		this.uniqueId = uniqueId;
	}
	public Integer getSubmitSum() {
		return submitSum;
	}
	public void setSubmitSum(Integer submitSum) {
		this.submitSum = submitSum;
	}
	public Integer getNoSubmitSum() {
		return noSubmitSum;
	}
	public void setNoSubmitSum(Integer noSubmitSum) {
		this.noSubmitSum = noSubmitSum;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public Integer getPassSum() {
		return passSum;
	}
	public void setPassSum(Integer passSum) {
		this.passSum = passSum;
	}
	public Integer getSum() {
		return sum = (this.passSum == null ? 0 : this.passSum) + (this.noSubmitSum == null ? 0 : this.noSubmitSum) + (this.submitSum == null ? 0 : this.submitSum);
	}
	public void setSum(Integer sum) {
		this.sum = sum;
	}
	public String getFillType() {
		return fillType;
	}
	public void setFillType(String fillType) {
		this.fillType = fillType;
	}
	public String getFillTypeName() {
		return fillTypeName;
	}
	public void setFillTypeName(String fillTypeName) {
		this.fillTypeName = fillTypeName;
	}
	public Integer getAllSum() {
		return allSum;
	}
	public void setAllSum(Integer allSum) {
		this.allSum = allSum;
	}
}
